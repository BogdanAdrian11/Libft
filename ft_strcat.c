char	*ft_strcat(char *dest, const char *src)
{
	int n;
	int i;

	n = 0;
	while (*(dest + n) != '\0')
	{
		n++;
	}
	i = 0;
	while (*(src + i) != '\0')
	{
		*(dest + n) = *(src + i);
		i++;
		n++;
	}
	*(dest + n) = '\0';
	return (dest);
}
